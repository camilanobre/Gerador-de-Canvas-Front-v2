            Se você é um empreendedor ou quer se tornar um,
            então é muito importante você entender o que é o Canvas!
          </strong>
        </p>
        <br>
        <p class="text-justify">
          O Canvas é uma ferramenta de planejamento estratégico, criada por Alexander Osterwalder,
          durante o seu doutorado na Universityof Lausanne, que permite desenvolver e esboçar modelos de negócio novos
          ou existentes.
        </p>
        <p class="text-justify">
          Muitos empreendedores ao iniciar um novo negócio ou o processo de lançamento de um novo produto,
          têm dificuldade em construir o melhor modelo de negócio e criar uma boa proposta de valor, perdendo muitas vezes,
          a oportunidade de criar negócios sustentáveis economicamente. Acabam preenchendo o canvas como obrigação, sem entender
          bem a relação entre cada uma das nove áreas do modelo, não têm a oportunidade de validar as hipóteses lançadas,
          ou preenchem o modelo “fora da ordem” de construção, fazendo com o que o preenchimento do canvas perca totalmente
          o sentido.
        </p>
        <h3 class="text-center">Como usar o Canvas para conhecer melhor o seu negócio</h3>
        <p>
          Modelo de negócios é como sua empresa cria, entrega e captura valor.
          Ou seja, como sua empresa resolve um problema para o cliente e como e quanto irá cobrar desse cliente.
          Para que isso aconteça, é indispensável que você saiba quem é o seu cliente, que problema você irá resolver,
          como irá resolver este problema e como pretende ganhar dinheiro.  Existem diversas ferramentas e metodologias
          para auxiliar empreendedores, como o Canvas.
        </p>
        <p>
          A metodologia baseia-se em um Canvas, em que estão dispostos os nove principais elementos de um modelo de negócio:
          segmentos de clientes, proposição de valor, canais, relacionamento com clientes, fontes de receita, recursos-chave,
          atividades-chave, parcerias-chave e estrutura de custos.
          Por ser muito visual e de fácil compreensão, o Canvas é uma ferramenta muito prática e possibilita
          refletir e identificar de forma simples os quatro aspectos fundamentais de um modelo de negócios em apenas
          uma página, uma tela: clientes, oferta, infraestrutura e viabilidade financeira.
        </p>
        <p>
          Podemos dividir o preenchimento do Canvas em 4 partes, respondendo previamente a 4 perguntas principais, que devem ser descritas em sequência:
        </p>
        <ul>
          <li>
            Primeiro, “coloque-se“ no lugar do seu cliente. “Viva“ o dia-a-dia dele e entenda quais são os problemas vividos
            por ele. Responda a pergunta: qual a proposta de valor gerada para o meu potencial cliente?
          </li>
          <p>
            Ao lançar um novo produto e construir um modelo de negócios, você não deve focar nas tecnologias que está lançando
            no mercado, por mais inovadoras que elas sejam, deixe-as de lado e apresente sempre quais os problemas que elas são
            capazes de resolver para o cliente. A solução que você leva para o seu potencial cliente é o valor que você cria para ele.
            Seus clientes não querem saber, por exemplo, se você utiliza um algoritmo inovador de machine learning para criar perfis de
            usuários a partir de uma base de dados, eles querem saber qual o valor que estes perfis de usuários podem geram para o
            negócio deles. Concluída esta etapa você terá respondido a principal pergunta do canvas.</p>
          <li>
            Em seguida, responda a pergunta: Para quem estou gerando valor? Sabendo qual é o valor esperado pelo seu cliente,
            identifique no mercado quais são as empresas/pessoas que “sentem a dor” do problema que o seu produto irá resolver.
          </li>
          <p>É fácil dizer que “o meu produto atende toda a população da classe C”, por exemplo.
          Mas provavelmente isto não será verdade, e fará com que você tenha dificuldade em vender
          o seu produto por não ter segmentado corretamente o seu mercado a partir da sua proposta de valor.
          Estabeleça para este segmento de mercado qual a melhor forma de se relacionar com estes clientes e
          quais são os melhores canais de distribuição de seus produtos (canais de venda). </p>
          <li>
            Sabendo qual é o valor que você irá gerar e conhecendo o seu cliente, responda a pergunta: Como fazer
            para entregar o valor gerado para o meu cliente?
          </li>
          <p>
            Observe que esta pergunta é bem completa e depende diretamente das respostas das duas perguntas anteriores.
            Quais as parcerias eu preciso estabelecer para entregar valor para o meu cliente? Quais as principais atividades
            eu preciso executar? Quais são os principais recursos externos para viabilizar o produto? Essas respostas irão estruturar
            o seu negócio e irão viabilizar a sua Proposta de Valor.
          </p>
          <li>
            Conhecendo a proposta de valor, sabendo quem é o seu cliente e o que deve fazer para entregar o produto para ele,
            responda a pergunta: Quanto vai custar?
          </li>
          <p>
            Neste tópico, entenda a perspectiva do seu cliente e defina quanto ele estará disposto a pagar pela sua solução.
            Quanto maior for o valor agregado gerado para o seu cliente e maior for a percepção dele, maior poderá ser o valor
            do seu produto.
          </p>
          <p>
            Defina também quanto custará para manter a sua solução. Relacione diretamente com as 3 respostas das perguntas anteriores.
            O valor cobrado pelo seu produto, ou serviço, deve possuir uma relação muito forte com a sua Proposta de Valor. E a forma de
            pagamento deve considerar se os clientes estão dispostos a utilizar os recursos de pagamento que você pretende estabelecer.
          </p>
        </ul>
        <h3 class="text-center">Como preencher o Canvas?</h3>
        <v-row
