        <p>
          Para o preenchimento de cada quadrante, Osterwalder sugere algumas perguntas que podem ajuda-lo na reflexão
          e no desenho de seu modelo de negócio:
        </p>
        <h3 class="text-center">Segmento de Clientes</h3>
        <p>
          Chegou a hora de definir para quem sua empresa vai vender.<br>
          Seus clientes podem ser pessoas físicas e/ou empresas e, se necessário, você pode trabalhar com mais de um público-alvo.
          Lembre-se de agrupar os clientes com o mesmo perfil.
          A dica é: procure sempre focar em um nicho de mercado. Não queira vender “tudo para todos”. <br>
          <span style="font-weight:bold">Algumas perguntas que podem ajudar : </span>
          <ul>
            <li>Quem está sendo impactado com os seus produtos ou serviços?</li>
            <li>Quais são seus clientes mais importantes?</li>
          </ul>
        </p>
        <h3 class="text-center">Proposta de Valor</h3>
        <p>
          Sua proposta de valor é a razão pela qual os clientes optam por comprar de você e não da concorrência.
          Nesse quadrante você precisa responder de forma simples e direta: qual solução entrego para o cliente?
          De que forma isso me diferencia?
          Se existirem grupos de clientes com necessidades diferentes, crie mais de uma proposta de valor.<br>
          <span style="font-weight:bold">Algumas perguntas que podem ajudar : </span>
          <ul>
            <li>Qual necessidade seu produto ou serviço satisfaz?</li>
            <li>De que forma sua solução ajuda o cliente?</li>
            <li>Que valor será entregue para o segmento de clientes? </li>
            <li>Quais são os problemas dos clientes que a empresa ajuda a resolver? </li>
            <li>Quais são os produtos e serviços que são entregues para cada segmento de clientes?  </li>
            <li>Que necessidades dos clientes estão sendo atendidas?</li>
          </ul>
        </p>
        <h3 class="text-center">Canais</h3>
        <p>
          Agora que você já sabe o que entrega e para quem entrega, chegou a hora de definir COMO você entrega.<br>
          <span style="font-weight:bold">As perguntas fundamentais nesse momento são : </span>
          <ul>
            <li>Como o cliente compra e recebe o produto/serviço?</li>
            <li>Em quais canais você pretende atuar para encaminhar vendas e solucionar
            eventuais problemas do consumidor em relação à sua empresa?</li>
            <li>Através de quais canais o segmento de clientes será alcançado? </li>
            <li>Como estão sendo alcançados hoje? </li>
            <li>Como os canais estão ligados entre si?</li>
            <li>Qual é o canal que funciona melhor?</li>
            <li>Qual é o canal que tem menor custo?</li>
          </ul>
          Alguns exemplos de canais são Correios, redes sociais, Whatsapp e 0800 (SAC).
        </p>
        <h3 class="text-center">Relacionamento com Clientes</h3>
        <p>
          Aqui, você precisa definir se o seu atendimento será por meio de uma página de perguntas e respostas,
          interação humana, chatbot comunidades no Facebook etc. <br>
          <span style="font-weight:bold">Algumas perguntas que podem ajudar:</span>
          <ul>
            <li>Que tipo de relacionamento cada segmento de clientes espera de você?</li>
            <li>Quais os custos desse atendimento?</li>
            <li>Como pretende se relacionar com os clientes? </li>
            <li>Que tipos de relacionamentos o segmento de clientes espera da empresa?</li>
            <li>Quais são os relacionamentos já estabelecidos hoje? </li>
            <li>Como estes relacionamentos estão integrados com outros aspectos do negócio?</li>
            <li> Quanto custa os relacionamentos existentes?</li>
          </ul>
        </p>
        <h3 class="text-center">Recursos-Chave</h3>
        <p>
          Que recursos-chave a proposta de valor exige? E os canais de entrega? E os relacionamentos? E as fontes de receita?
        </p>
        <h3 class="text-center">Atividades-Chave</h3>
        <p>As atividades-chave de um ecommerce são aquelas essenciais para manter a empresa funcionando e entregando todo
        o valor prometido.<br>
          <span style="font-weight:bold">Alguns exemplos são:</span> marketing, gerenciamento de redes sociais, criação de conteúdo audiovisual etc.
        </p>
        <h3 class="text-center">Parceiros-Chave</h3>
        <p>
          As parcerias principais são alianças de negócio que você fará com fornecedores e outros parceiros.
          <span style="font-weight:bold">Algumas perguntas que podem ajudar:</span>
          <ul>
            <li>Quais recursos principais você está buscando fora da empresa?</li>
            <li>Quem são os parceiros-chave? </li>
            <li>Quem são os fornecedores-chave? </li>
            <li>Que recurso-chave a empresa consegue dos parceiros-chave?</li>
            <li>Quais das atividades-chave são executadas por parceiros?</li>
          </ul>
        </p>
        <h3 class="text-center">Fontes de Receita</h3>
        <p>
          Como a sua empresa gera receita? Esta é a pergunta a ser respondida aqui. Dependendo do modelo de negócios,
          você pode ter diversas fontes de receita.
          Por exemplo: “minha empresa gera receita vendendo produtos e prestando consultorias sobre o mercado” ou
          “minha empresa gera receita por um sistema de assinaturas mensais”. <br>
          <ul>
            <li>Por qual valor o segmento de clientes está disposto a pagar? </li>
            <li>Pelo quê pagam atualmente?</li>
            <li>Como o segmento de clientes paga hoje? </li>
            <li>Como eles prefeririam pagar? </li>
            <li>Qual é a contribuição de cada fonte de receita para receita total?</li>
          </ul>
        </p>
        <h3 class="text-center">Estrutura de Custo</h3>
        <p>
          No último quadrante, você preencherá todos os custos relevantes necessários para o seu modelo de negócios funcionar.
          Esses custos podem ser fixos ou variáveis (aluguel, softwares, salários, manutenção de maquinaria, campanhas de
          marketing etc).<br>
          <ul>
            <li>Quais são os custos mais importantes do modelo de negócios?</li>
            <li>Quais são os recursos-chave mais caros?</li>
            <li>Quais são as atividades-chave mais caras?</li>
          </ul>
        </p>
